a  = 2
b = 2

# Arithmetic Operators

print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a//b)
print(a**b)
print(a%b)

#Assignment Operators

c = 5
c += 5
c-= 1
c *= 2
c /= 5
c //=2
c **= 3
c %= 2

print(c)

#Comparison Operators

print(a==b)
print(a!=b)
print(a>b)
print(a<b)
print(a>=b)
print(a<=b)

#Logical Operators

print(a<5 or a>10)
print(a<5 and a>10)
print(not(a>5))

